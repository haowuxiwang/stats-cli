class TestMultipleComparison:
    """Cross-validate multiple comparison against R."""

    def test_tukey_hsd(self):
        groups = [IRIS_DATA["setosa_sl"], IRIS_DATA["versicolor_sl"], IRIS_DATA["virginica_sl"]]
        r = multiple_comparison(test_type="tukey", groups=groups)
        ref = REF["tukey_hsd"]

        # Map group indices to names
        group_names = ["setosa", "versicolor", "virginica"]
        for pair_name, ref_vals in ref.items():
            # Find matching pair in our results
            our_pair = None
            for pair in r["comparisons"]:
                name_i = group_names[pair["group_i"]]
                name_j = group_names[pair["group_j"]]
                key = f"{name_j}-{name_i}"
                if key == pair_name:
                    our_pair = pair
                    break
            assert our_pair is not None, f"Pair {pair_name} not found in results"
            # Mean diff may differ in sign depending on subtraction order
            assert_close(abs(our_pair["mean_diff"]), abs(ref_vals["diff"]), abs_tol=1e-6)
            assert_close(our_pair["p_value"], ref_vals["p_adj"], rel_tol=0.01)


