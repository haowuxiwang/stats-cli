class TestTTest:
    """Cross-validate t-tests against R."""

    def test_one_sample(self):
        r = ttest(test_type="one_sample", values=IRIS_DATA["setosa_sl"], mu=5.0)
        ref = REF["ttest_one_sample"]

        assert_close(r["t_statistic"], ref["t_stat"], abs_tol=1e-6)
        assert_close(r["p_value"], ref["p_value"], abs_tol=1e-4)
        assert_close(r["ci_95"][0], ref["ci_lower"], abs_tol=1e-3)
        assert_close(r["ci_95"][1], ref["ci_upper"], abs_tol=1e-3)
        assert r["n"] == 50

    def test_two_sample_welch(self):
        r = ttest(
            test_type="two_sample",
            values=IRIS_DATA["setosa_sl"],
            values2=IRIS_DATA["versicolor_sl"],
        )
        ref = REF["ttest_two_sample_welch"]

        assert_close(r["t_statistic"], ref["t_stat"], abs_tol=1e-6)
        assert_close(r["p_value"], ref["p_value"], abs_tol=1e-10)
        assert_close(r["mean_difference"], ref["mean_diff"], abs_tol=1e-6)

    def test_paired(self):
        before = [85, 88, 90, 82, 87, 91, 86, 89, 84, 88]
        after = [88, 91, 93, 86, 90, 95, 89, 92, 87, 91]
        r = ttest(test_type="paired", values=before, values2=after)
        ref = REF["ttest_paired"]

        assert_close(r["t_statistic"], ref["t_stat"], abs_tol=1e-6)
        assert_close(r["p_value"], ref["p_value"], abs_tol=1e-6)
        assert_close(r["ci_95"][0], ref["ci_lower"], abs_tol=1e-3)
        assert_close(r["ci_95"][1], ref["ci_upper"], abs_tol=1e-3)


