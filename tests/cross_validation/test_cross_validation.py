class TestNonparametric:
    """Cross-validate non-parametric tests against R."""

    def test_mann_whitney(self):
        g1 = [23, 25, 28, 30, 32, 35, 27, 29]
        g2 = [31, 33, 36, 38, 40, 42, 35, 37]
        r = nonparametric(test_type="mann_whitney", x=g1, y=g2)
        ref = REF["mann_whitney"]

        assert_same_conclusion(r["p_value"], ref["p_value"])

    def test_kruskal_wallis(self):
        g1 = [7, 14, 14, 13, 12, 9, 6, 14, 12, 7]
        g2 = [15, 17, 13, 15, 15, 13, 9, 12, 10, 6]
        g3 = [6, 7, 11, 8, 7, 6, 10, 5, 4, 9]
        r = nonparametric(test_type="kruskal_wallis", groups=[g1, g2, g3])
        ref = REF["kruskal_wallis"]

        assert_close(r["h_statistic"], ref["chi2_stat"], abs_tol=1e-4)
        assert_same_conclusion(r["p_value"], ref["p_value"])

    def test_wilcoxon_signed_rank(self):
        x = [1.83, 0.50, 1.62, 2.48, 1.68, 1.88, 1.55, 3.06, 1.30]
        y = [0.878, 0.647, 0.598, 2.05, 1.06, 1.29, 1.06, 3.14, 1.29]
        r = nonparametric(test_type="wilcoxon", x=x, y=y)
        ref = REF["wilcoxon_signed_rank"]

        assert_same_conclusion(r["p_value"], ref["p_value"])

    def test_chi_square(self):
        contingency = [[50, 30], [20, 60]]
        r = nonparametric(test_type="chi_square", observed=contingency)
        ref = REF["chi_square"]

        assert_close(r["chi2_statistic"], ref["chi2_stat"], abs_tol=1e-4)
        assert_same_conclusion(r["p_value"], ref["p_value"])
        assert r["df"] == ref["df"]


