# Tolerance helpers
def assert_close(actual, expected, abs_tol=1e-6, rel_tol=1e-4, label=""):
    """Assert two floats are close within tolerance."""
    if expected == 0:
        assert abs(actual) < abs_tol, f"{label}: got {actual}, expected ~0 (abs_tol={abs_tol})"
    else:
        diff = abs(actual - expected)
        rel = diff / abs(expected)
        assert diff < abs_tol or rel < rel_tol, (
            f"{label}: got {actual}, expected {expected} (abs_diff={diff:.2e}, rel_diff={rel:.2e})"
        )


def assert_same_conclusion(actual_p, expected_p, alpha=0.05):
    """Assert both p-values lead to the same significance conclusion."""
    actual_sig = actual_p < alpha
    expected_sig = expected_p < alpha
    assert actual_sig == expected_sig, (
        f"Conclusion mismatch: p={actual_p:.6f} ({'sig' if actual_sig else 'ns'}) "
        f"vs p={expected_p:.6f} ({'sig' if expected_sig else 'ns'})"
    )


