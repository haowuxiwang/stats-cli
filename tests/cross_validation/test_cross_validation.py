class TestFactorAnalysis:
    """Cross-validate factor analysis structure."""

    def test_two_factor_structure(self):
        # Generate data with known 2-factor structure:
        # Vars 1-3 load on factor 1, vars 4-6 load on factor 2
        np.random.seed(42)
        n = 100
        f1 = np.random.normal(0, 1, n)
        f2 = np.random.normal(0, 1, n)
        v1 = 0.9 * f1 + np.random.normal(0, 0.3, n)
        v2 = 0.8 * f1 + np.random.normal(0, 0.3, n)
        v3 = 0.7 * f1 + np.random.normal(0, 0.3, n)
        v4 = 0.9 * f2 + np.random.normal(0, 0.3, n)
        v5 = 0.8 * f2 + np.random.normal(0, 0.3, n)
        v6 = 0.7 * f2 + np.random.normal(0, 0.3, n)
        data = np.column_stack([v1, v2, v3, v4, v5, v6])

        r = multivariate(
            analysis_type="factor_analysis",
            values=data,
            n_factors=2,
            rotation="varimax",
        )

        ref = REF["factor_analysis"]
        # All communalities should be reasonably high (variables are well-explained)
        for var_name, comm in r["communalities"].items():
            assert comm >= ref["r_communality_min"], (
                f"Communality for {var_name} = {comm}, expected >= {ref['r_communality_min']}"
            )
        # Total variance explained should be substantial
        assert r["total_variance_explained"] >= ref["r_total_variance_min"], (
            f"Total variance explained = {r['total_variance_explained']}, expected >= {ref['r_total_variance_min']}"
        )
        # Loadings matrix should have 6 variables x 2 factors
        assert len(r["loadings"]) == 6, f"Expected 6 variables in loadings, got {len(r['loadings'])}"
        for var_name, loads in r["loadings"].items():
            assert len(loads) == 2, f"Expected 2 factors for {var_name}, got {len(loads)}"


class TestMANOVA:
    """Cross-validate MANOVA with clearly different groups."""

    def test_manova_significant(self):
        # Three groups with clearly different means on 2 DVs
        np.random.seed(42)
        group1 = np.column_stack(
            [
                np.random.normal(10, 1, 20),
                np.random.normal(20, 1, 20),
            ]
        )
        group2 = np.column_stack(
            [
                np.random.normal(15, 1, 20),
                np.random.normal(25, 1, 20),
            ]
        )
        group3 = np.column_stack(
            [
                np.random.normal(20, 1, 20),
                np.random.normal(30, 1, 20),
            ]
        )

        r = multivariate(
            analysis_type="manova",
            groups=[group1, group2, group3],
        )

        ref = REF["manova"]
        # Wilks' Lambda p-value should be significant (groups are clearly different)
        p_wilks = r["p_values"]["wilks_lambda"]
        assert p_wilks < ref["r_p_max"], f"Wilks' Lambda p = {p_wilks}, expected < {ref['r_p_max']}"
        # Pillai's Trace should also be significant
        p_pillai = r["p_values"]["pillai_trace"]
        assert p_pillai < ref["r_p_max"], f"Pillai's Trace p = {p_pillai}, expected < {ref['r_p_max']}"
        # Wilks' Lambda should be small (close to 0 means strong effect)
        assert r["statistics"]["wilks_lambda"] < 0.5, (
            f"Wilks' Lambda = {r['statistics']['wilks_lambda']}, expected < 0.5"
        )
