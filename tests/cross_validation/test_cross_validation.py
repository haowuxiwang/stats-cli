class TestBayesianTTest:
    """Cross-validate Bayesian t-test against R BayesFactor::ttestBF().

    Our implementation uses BIC approximation (Wagenmakers 2007) while R uses
    the JZS (Jeffreys-Zellner-Siow) prior. These yield very different absolute
    BF10 values but agree on direction and qualitative conclusion. We verify:
    1. Both agree there is strong evidence for H1 (BF10 > 3)
    2. The classical t-test and p-value match R closely
    """

    def test_two_sample_bayesian_ttest(self):
        ref = REF["bayesian_ttest"]
        r = bayesian("ttest", values=ref["values"], values2=ref["values2"])

        bf10 = r["bayes_factor_10"]
        assert bf10 > 0, f"BF10 must be positive, got {bf10}"
        # Both BIC and JZS agree on strong evidence for H1 for this data
        assert bf10 > 3, f"BF10 = {bf10}, expected > 3 (moderate evidence for H1)"
        # The t-statistic should be large and p-value small (same underlying test)
        assert abs(r["t_statistic"]) > 3, f"t = {r['t_statistic']}, expected |t| > 3"
        assert r["p_value"] < 0.01, f"p = {r['p_value']}, expected < 0.01"


