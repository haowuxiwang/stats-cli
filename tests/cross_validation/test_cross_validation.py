class TestDescriptive:
    """Cross-validate descriptive statistics against R."""

    def test_iris_setosa_descriptive(self):
        r = descriptive(values=IRIS_DATA["setosa_sl"])
        ref = REF["descriptive_iris_setosa"]

        assert r["n"] == ref["n"]
        assert_close(r["mean"], ref["mean"], abs_tol=1e-10)
        assert_close(r["std"], ref["sd"], abs_tol=1e-10)
        assert_close(r["median"], ref["median"], abs_tol=1e-10)
        assert_close(r["min"], ref["min"], abs_tol=1e-10)
        assert_close(r["max"], ref["max"], abs_tol=1e-10)
        assert_close(r["q1"], ref["q1"], abs_tol=1e-10)
        assert_close(r["q3"], ref["q3"], abs_tol=1e-10)


